// Function to find the node with the minimum distance value
int mindistance(int distance[], bool stat[])
{
	int minimum = INT_MAX;

	// To store the index of the node with the minimum distance
	int ind;
	for (int k = 0; k < 81; k++)
	{
		if (stat[k] == false && distance[k] <= minimum)
		{
			minimum = distance[k];
			ind = k;
		}
	}
	return ind;
}

// Dijkstra's algorithm implementation
void dijkstra(int graph[81][81], int source, string stations[81])
{
	int distance[81];
	bool stat[81];

	// Initialize distance and stat arrays
	for (int k = 0; k < 81; k++)
	{
		distance[k] = INT_MAX;
		stat[k] = false;
	}

	distance[source] = 0;

	// Find the shortest path for all nodes
	for (int k = 0; k < 81; k++)
	{
		int m = mindistance(distance, stat);
		stat[m] = true;

		// Update distance values of adjacent nodes
		for (int k = 0; k < 81; k++)
		{
			if (!stat[k] && graph[m][k] && distance[m] != INT_MAX && distance[m] + graph[m][k] < distance[k])
				distance[k] = distance[m] + graph[m][k];
		}
	}

	// Print the shortest distances from the source node to every other node
	cout << "Minimum number of stations from " << stations[source] << " to every other station:" << endl;
	for (int k = 0; k < 81; k++)
	{
		cout << "Number of stations from " << stations[source] << " To " << stations[k] << " are " << distance[k] << endl;
	}
}

